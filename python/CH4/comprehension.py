# Comprehension in Python is a concise way to create lists, sets, or dictionaries.
# It allows you to generate these data structures in a single line of code, making your code more readable and efficient.
# Comprehensions can include conditions, nested loops, and function calls, providing a powerful way
# Syntax of comprehension: [expression for item in iterable if condition]
# types of expressions: list, set, tuple, dictionary
# all possible expressions: x,x*x,(x,y),x.upper(),x.upper() if len(x) > 4, x.upper() if len(x) > 4 and x.startswith("h"), (x,y) if len(x) > 4 and len(y) > 4, (x,y) if len(x) > 4 and len(y) > 4
# all possible conditions: if condition, if condition and condition, if condition or condition 
# all possible nested loops: for x in iterable, for x in iterable for y in iterable, for x in iterable if condition, for x in iterable if condition and condition
