# queue is a data structure that follows the First In First Out (FIFO) principle.
# It allows you to add elements to the end and remove elements from the front, ensuring that the first element added is the first one to be removed.
class Queue:
    def __init__(self):
        self.items = []

    def enqueue(self, item):
        self.items.append(item)

    def dequeue(self):
        if not self.is_empty():
            return self.items.pop(0)
        return None

    def peek(self):
        if not self.is_empty():
            return self.items[0]
        return None

    def is_empty(self):
        return len(self.items) == 0

    def size(self):
        return len(self.items)
